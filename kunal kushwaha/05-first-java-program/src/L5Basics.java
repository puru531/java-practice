public class L5Basics {
    public static void main(String[] args) {

        //while loop : use while loop when you don't know how many times the loop is going to run
        int count = 1;
        while(count != 5) {
            System.out.println(count);
            count++;
        }

        //for loop : use for loop when you know many times the loop is going to run
        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }


    }
}
