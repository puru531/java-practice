public class Primitives {
    public static void main(String[] args) {
        //Primitives : any data type which you cannot break even further.
        int rollno = 64;
        char letter = 'p';
        float marks = 98.67f; //all decimals are by default double, in order to get more accurate value of decimal, we use float, which is denoted by adding f at the last of the decimal number.
        double largeDecimalNumbers = 456466.6546;
        long largerIntegerValue = 465465465465655354L;
        boolean check = true;


        //Each primitive data types have their classes also:
        Integer rno = 64;




        //String is not primitive
        String  name = "Purushottam";
    }
}
